package day07_unary_operators;

public class KgtoPounds {
    public static void main(String[] args) {
        double kg = 2.20462;
        double kg10 = kg*10;
        System.out.println(" 10 kg equals to " + kg10 + " pounds");
        double kg20 = kg*20;
        System.out.println( " 20 kg equals to " + kg20 + " pounds");

    }


}
